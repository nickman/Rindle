package org.helios.pag.store.redis;

/**
 * <p>Title: ClientInfoProvider</p>
 * <p>Description: Defines a class that can provide a {@link ClientInfo} instance</p> 
 * <p>Company: Helios Development Group LLC</p>
 * @author Whitehead (nwhitehead AT heliosdev DOT org)
 * <p><code>org.helios.pag.store.redis.ClientInfoProvider</code></p>
 */

public interface ClientInfoProvider {
	/**
	 * Returns this object's {@link ClientInfo} instance
	 * @return this object's {@link ClientInfo} instance
	 */
	public ClientInfo getClientInfo();
	
	/**
	 * Indicates if this provider is connected
	 * @return true if this provider is connected, false otherwise
	 */
	public boolean isConnected();
}
